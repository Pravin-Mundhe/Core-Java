public class PrimitiveDataTypes 
{
	public static void main(String[] args) 
	{
		byte b=15;
		short s=20;
		int i=5;		
		float f=3.64F;
		long l=12345678L;
		double d=10.4568924D;
		boolean bl=true;
		char a='z';
		
		System.out.println("value of byte variable is: "+b);
		System.out.println("value of short variable is: "+s);
		System.out.println("value of integer variable is: "+i);
		System.out.println("value of float variable is: "+f);
		System.out.println("value of long variable is: "+l);
		System.out.println("value of double variable is: "+d);
		System.out.println("value of boolean variable is: "+bl);
		System.out.println("value of character variable is: "+a);
		
		System.out.println("Integer value of Binary value 0b10 is "+0b10); 
		System.out.println("Integer value of Hexadecimal value 0x32 is "+0x32); 
		System.out.println("Integer value of Octal value 017 is "+017); 
		
		
	}

}
