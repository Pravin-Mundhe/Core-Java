
public class TwoDArrayDemo 
{
	public static void main(String[] args)
	{
		//2-D array
		int[][] a= {
					{1,2},
					{3,4}
	               }; 
		
		System.out.println("a[0][0] is: "+a[0][0]);
		System.out.println("a[0][1] is: "+a[0][1]);
		System.out.println("a[1][0] is: "+a[1][0]);
		System.out.println("a[1][1] is: "+a[1][1]);
		
		//Declaring 2d array
		int[][] b=new int[3][3]; 
		//int[][] b;
		//b=new int[3][3];
	}
	
}
